while True:
      chân = int(input("Tao đố mày 1 câu nhé. Nhện có mấy chân: 1. ko có, 2. 4 chân 3. 8 chân 4. 12 chân"))
      if chân==1:
         print("Sai rồi, về quê làm ruộng đi")
      if chân==2:
         print("Ko bằng đứa lớp 1!")
      if chân==4:
         print("não mày để đâu hả?")
      if chân==3:
          print("Đúng rồi mày giỏi lắm")
          break
      else:
          print("Xem lại câu hỏi đi, chỉ trả lời 1 hay 2 hay 3 hay 4 thôi, khổ quá, ăn gì mà ngu thế")